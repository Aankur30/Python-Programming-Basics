x=2;
y=3;
print(x==y);#yes it will give false as we are comparing x and y which are not equal

"""!= operator """
print(x!=y);#this will give true as x is not equal to y

#other operators are greater than and smaller than and then greater than or equal to and smaller than or equal to
#logical operators
print(x!=y and y>x);
print(x!=y or y>x);
print(not(x!=y and y>x));